package main

import "fmt"

func main() {
	fmt.Println("Nurul")
	fmt.Println("Nurul Mustofa")
	fmt.Println("Muhammad Nurul Mustofa")

	//	len untuk menghitung jumlah karakter di string
	// "string"[number] Mengambil karakter pada posisi yang ditentukan
	fmt.Println(len("Nurul"))
	fmt.Println("Nurul Mustofa"[0])
	fmt.Println("Muhammad Nurul Mustofa"[1])
}
